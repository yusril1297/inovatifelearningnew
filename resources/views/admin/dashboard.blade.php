<script>
    // Function to animate number counting up
    function animateNumber(targetId, start, end, duration) {
        let startTime = null;
        let element = document.getElementById(targetId);

        // This function is called on each frame
        function countUp(currentTime) {
            if (!startTime) startTime = currentTime;
            let progress = currentTime - startTime;
            let value = Math.min(start + (end - start) * (progress / duration), end); // Calculate current value
            element.innerText = Math.floor(value); // Set the text content of the element

            if (progress < duration) {
                requestAnimationFrame(countUp); // Continue animation until duration is met
            } else {
                element.innerText = end; // Ensure it ends exactly at the target value
            }
        }

        requestAnimationFrame(countUp); // Start the animation
    }

    // Call the function with appropriate parameters (target ID, start value, end value, duration in ms)
    animateNumber('total-orders', 0, 4805, 2000); // Animation from 0 to 4805 in 2 seconds
</script>

