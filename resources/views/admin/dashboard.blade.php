    // Fungsi untuk menampilkan angka dengan animasi
    function animateNumber() {
        const revenueElement = document.getElementById("revenue");
        let startValue = 342.450;
        let endValue = 842.450; // Nilai akhir
        let duration = 0; // Durasi animasi dalam milidetik
        let stepTime = Math.abs(Math.floor(duration / endValue));
        
        let interval = setInterval(function() {
            startValue++;
            revenueElement.textContent = "Rp." + startValue.toLocaleString();
            if (startValue === endValue) {
                clearInterval(interval);
            }
        }, stepTime);
    }

    // Memulai animasi saat halaman dimuat
    window.onload = animateNumber;
</script>



                               
